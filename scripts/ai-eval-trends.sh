#!/bin/bash
set -euo pipefail

# Regenerates trends.md in the private AI Implementation Evals Gist.
# Run manually or via scheduled Actions workflow to surface long-term trends.
#
# Usage: bash scripts/ai-eval-trends.sh

echo "=== Regenerating AI eval trends ===" >&2

if [[ -z "${GH_PR_CREATE_TOKEN:-}" ]]; then
  echo "Error: GH_PR_CREATE_TOKEN not set. Set it to a token with gist scope." >&2
  exit 1
fi

EVALS_GIST_ID=$(GH_TOKEN="$GH_PR_CREATE_TOKEN" gh gist list --limit 100 2>/dev/null |
  grep 'AI Implementation Evals' | awk '{print $1}' | head -1 || true)

if [[ -z "$EVALS_GIST_ID" ]]; then
  echo "No evals Gist found yet — nothing to analyze." >&2
  exit 0
fi

# Verify Gist is private
GIST_PUBLIC=$(GH_TOKEN="$GH_PR_CREATE_TOKEN" gh api "/gists/${EVALS_GIST_ID}" \
  --jq '.public' 2>/dev/null || echo "unknown")
if [[ "$GIST_PUBLIC" == "true" ]]; then
  echo "SECURITY: Evals Gist ${EVALS_GIST_ID} is PUBLIC — aborting." >&2
  exit 1
fi

echo "Reading evals data from Gist ${EVALS_GIST_ID}..." >&2

RUNS=$(GH_TOKEN="$GH_PR_CREATE_TOKEN" gh api "/gists/${EVALS_GIST_ID}" \
  --jq '.files["runs.jsonl"].content' 2>/dev/null || echo "")

if [[ -z "$RUNS" ]]; then
  echo "Evals Gist exists but runs.jsonl is empty — nothing to analyze." >&2
  exit 0
fi

TOTAL=$(echo "$RUNS" | grep -c '.' || echo 0)
echo "Found ${TOTAL} runs" >&2

# Parse runs into individual JSON objects
RUNS_JSON=$(echo "$RUNS" | jq -s '.' 2>/dev/null || echo "[]")

# Compute trends
VERDICT_COUNTS=$(echo "$RUNS_JSON" | jq 'group_by(.verdict) | map({verdict: .[0].verdict, count: length})')
AVG_ITERATIONS=$(echo "$RUNS_JSON" | jq '[.[] | select(.verdict != "UNKNOWN")] | if length > 0 then (map(.iterations) | add / length | . * 10 | round / 10) else "N/A" end')
AVG_CRITICAL=$(echo "$RUNS_JSON" | jq '[.[] | select(.verdict != "UNKNOWN")] | if length > 0 then (map(.critical) | add / length | . * 10 | round / 10) else "N/A" end')
AVG_HIGH=$(echo "$RUNS_JSON" | jq '[.[] | select(.verdict != "UNKNOWN")] | if length > 0 then (map(.high) | add / length | . * 10 | round / 10) else "N/A" end')
AVG_DURATION=$(echo "$RUNS_JSON" | jq '[.[] | select(.verdict != "UNKNOWN")] | if length > 0 then (map(.duration_s) | add / length / 60 | round) else "N/A" end')

# Per-prompt-version stats
PROMPT_STATS=$(echo "$RUNS_JSON" | jq '
  [.[] | select(.verdict != "UNKNOWN")]
  | group_by(.prompt_ver)
  | map({
      prompt_ver: .[0].prompt_ver,
      count: length,
      avg_iterations: (map(.iterations) | add / length | . * 10 | round / 10),
      avg_critical: (map(.critical) | add / length | . * 10 | round / 10),
      safe_pct: ((map(select(.verdict == "SAFE_TO_PUSH")) | length) / length * 100 | round)
    })
')

# Last 10 runs for recent trend
LAST_10=$(echo "$RUNS_JSON" | jq 'sort_by(.date) | .[-10:] | map({date, issue, verdict, iterations, critical, high})')

TODAY=$(date -u +%Y-%m-%d)

cat >/tmp/trends.md <<EOF
# AI Implementation Trends

Last updated: ${TODAY} · Total runs: ${TOTAL}

## Verdict Distribution

\`\`\`
$(echo "$VERDICT_COUNTS" | jq -r '.[] | "\(.verdict): \(.count)"')
\`\`\`

## Averages (excluding UNKNOWN verdicts)

| Metric | Average |
|--------|---------|
| Review iterations | ${AVG_ITERATIONS} |
| Critical findings | ${AVG_CRITICAL} |
| High findings | ${AVG_HIGH} |
| Duration (min) | ${AVG_DURATION} |

## Per Prompt Version

\`\`\`
$(echo "$PROMPT_STATS" | jq -r '.[] | "Prompt \(.prompt_ver): \(.count) runs | avg \(.avg_iterations) iters | \(.safe_pct)% SAFE_TO_PUSH"')
\`\`\`

## Last 10 Runs

\`\`\`
$(echo "$LAST_10" | jq -r '.[] | "\(.date) | #\(.issue) | \(.verdict) | \(.iterations) iter | C\(.critical) H\(.high)"')
\`\`\`

---
*Generated by \`scripts/ai-eval-trends.sh\` — data from private "AI Implementation Evals" Gist (#${EVALS_GIST_ID})*
EOF

# Update the Gist with the new trends
jq -n \
  --arg content "$(cat /tmp/trends.md)" \
  '{"files":{"trends.md":{"content":$content}}}' |
  GH_TOKEN="$GH_PR_CREATE_TOKEN" gh api "/gists/${EVALS_GIST_ID}" \
    --method PATCH --input - >/dev/null 2>&1

echo "Trends updated in Gist ${EVALS_GIST_ID}" >&2

# Print trends to stdout for local visibility
cat /tmp/trends.md
